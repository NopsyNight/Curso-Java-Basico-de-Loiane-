class HelloWorld {
    public static void main(String[] args){
        System.out.println("Hello, World!");
    }

}
// Class = Declaração de uma classe
// HelloWorld = Nome da classe
// public = Modificador de acesso
// static = Pertence à classe e não a instâncias
// void = Tipo de retorno do método (nenhum valor retornado)
// main = Nome do método principal
// String[] args = Parâmetro do método main (array de strings) e argumentos de linha de comandos
// System.out.println = Comando para imprimir no console